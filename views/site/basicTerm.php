        <h1 style="text-align:center">【FD的基本术语】</h1>
        <p>1、顺时针方向：英文称为Clockwise，简写为C.W.，就是与时针转动方向相同之方向，也就是当右肩朝圆心时，所面对的方向，或面朝圆心时之左边。</p>
        <p>2、反时针方向：英文称为Counterclockwise，简写为C.C.W.）就是与时针移动方向相反之方向，也就是当左肩朝圆心时，所面对的方向，或面朝圆心时之右边。</p>
        <p>3、方向线：英文称为Line of Direction，简写为L.O.D.，有人译为舞程线，为舞蹈者在舞场中行进的一般方向，亦即反时针方向，因此有的书上以反时针方向代之。</p>
        <p>4、反方向线：英文称为Reverse Line of Direction，简写为R.L.O.D.，有人译为反舞程线，方向线之相反者，有时以顺时针方向代之。</p>
        <p>5、面对面：英文称为Face to face 或 Vis-a-vis，舞伴正面相对。</p>
        <p>6、背对背：英文称为Back to back，舞伴背相对。</p>
        <p>7、舞伴：英文称为Partner，系泛指共舞者，惟通常指双人舞之共舞异性。</p>
        <p>8、邻伴：英文称为Corner，系泛指邻人，惟通常指方舞或混合舞中之邻近异性，如男之左女，女之右男。</p>
        <p>9、对面者：英文称为Opposite，系指舞中对面者，常在方舞或四人一组之行进型舞中使用。</p>
        <p>10、主对：英文称为Head Couple，或简称为Heads，在方舞中指第一、三对：即背对、面对音乐台之两对（Top & Bottom），此处之一、三对顺序系根据美国方舞，而古代之欧洲方舞，则不一定是如此之顺序，但主对一定是背对与面对音乐台者。英文中之Head Couple系指第一对。主对在菲律宾舞中称之为Cabeceras。另外，英文之Head系指靠近舞台之一端。</p>
        <p>11、副对：英文称为Side Couples，或简称为Sides，在方舞中指第二、四对。副对在菲律宾舞中称之为Costados。另外，英文之Side系指舞场两侧。</p>
        <p>12、重心足：英文称为Supporting Foot，指舞者全部或大部之体重置于其上之一足。</p>
        <p>13、自由足：英文称为Free Foot，即非重心足，可以自由活动之一足。</p>
        <p>14、自由手：英文称为Free Hand，指没有指定动作或不属于某种舞姿限制的手，如牵内手时之外手。</p>
        <p>15、内足：英文称为 Inside Foot，即有舞伴时，指靠近舞伴之一足，无舞伴时，则指靠近指定邻人或圆心之一足。</p>
        <p>16、外足：英文称为Outside Foot，指与内足相反之一足。</p>
        <p>17、内手：英文称为Inside Hand，指有舞伴时，指靠近舞伴之一手，无舞   伴时，则指靠近指定邻人或圆心之一足。</p>
        <p>18、外手：英文称为Outside Hand，指与内手相反之一手。</p>
        <p>19、内转（Turn in toward......）：有舞伴时，指向靠近舞伴之方向转动，无舞伴时，则指向圆心、自己身体、指定人物或对象之方向转动。</p>
        <p>20、外转（Turn away from......）：向内转方向相反之方向转动。</p>
        <p>21、菱形路腺：在英文中有时称为Diamond，是土风舞常见的行进路线，用指定的舞步循菱形路线，与舞伴做斜向分开、斜向靠近之动作。</p>
