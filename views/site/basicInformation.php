        <p>
        一、单列队形，英文称为Single-line Formation，分为以下数种：<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（一）卡瓦特队形:英文称为Gavotte Formation或GavotteSet，男女间隔成纵列：最前者为男子；右手向后，与后一人之左手互勾小指，前后两端者之自由手插腰。<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（二）链状队形: 英文称为Chain Formation，不分舞伴成单列，人数不多，依指定方式与邻人连接。<br>
        </p>
        <p>
        二、圆圈队形，英文称为Circles，分为以下各种：<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（一）半圈或开口圈<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;英文称为Semi-Circle，Open-Circle或Broken-circle；不分舞伴，全体与邻人依照指定方式牵手、勾小指、搭肩、持手帕连接并面对指定方向，二端者不牵手，通常最右端者为领导者，右手持一手帕；这种队形在巴尔干半岛最常见。<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（二）闭口圈<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;英文称为Closed Circle或Circle Formation，即舞蹈者成封闭之圆圈队形，有单圈、双重圆、三重圆、四重圆等种类，其中又因换伴与否，分为混合舞（Mixer）与圆圈舞（Circle Dance）二类。<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（三）单圈: 英文称为Single Circle，又分为下面二种：<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1、无伴单圈（Single Circle Without Partner）。<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2、有伴单圈（Single Circle With Partner）。<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;其队形方式有二种：<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1、面对圆心之单圈（Single Circle, Facing In）<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;不论有无舞伴，全体面向圆心，依照指定方式与邻人连接，若有     舞伴，一般为男左女右。<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2、面对舞伴之单圈（Single Circle, Facing Partner）<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;舞伴相对，一般为男朝方向线，女朝反方向线。<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;除此之外，单圈队形有时依照指定而面对方向线、反方向线或其它指定方向。有时为了舞蹈需要，做成二个或二个以上之同心圆。<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（四）双重圆:  英文称为Double Circle，分为下列几种：<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1、舞伴相对之双重圆（Double Circle, Partners Facing）~男内女外成双重圆，舞伴相对，男朝圆外，女朝圆心。男内女外为通例，亦有特殊指定男外女内者。<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2、面朝方向线之双重圆（Double Circle, Facing L.O.D. or Counterclockwise）~男内女外成双重圆，面朝方向线，舞伴成男左女右并肩站立。亦有特别指定面朝反方向线者（Double circle, Facing R.L.O.D. or Clockwise）。<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3、二对相向（Duple miner Set）~<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;双重圆，两对相向，一朝方向线，一朝反方向线；依照指定方式，为男内女外，或舞伴男左女右并肩站立；若系后者，则朝方向线者男内女外，朝反向线者，男外女内。这种队形有时称为Progressive Formation，但并不限于双重圆，有时为三重圆或四重圆。<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;4、一对在外，一对在内之双重圆（Double Circle, odd couple facing in, even couple facing out）~双重圆，一对一组，一朝圆心，一朝圆外，舞伴系男左女右并肩站立。<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（五）三重圆<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Triple circle, Trio Circle或Trios，分为下列两种：<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1、三重圆 ，面朝方向线（Triple Circle Facing L.O.D. or Counterclockwise）~三重圆，三人一组并肩站立，面朝方向线，通常为一男二女组成，男在中间，女在两侧。<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2、三重圆，二组相对（Triple Circle in Two Lines Facing）~三重圆，三人一组并肩站立，两组相对，一朝方向线，一朝反方向线。<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3、自由队形~三人一组，自由散处。<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（六）四重圆<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;英文称为Quardruple Circle，分为下列两种：<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1、四重圆，面朝方向线（Quardruple Circle, Facing L.O.D. or Counterclockwise）~四人（二男二女）一组并肩站立，同组男女间隔，舞伴男左女右，面朝方向线。<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2、四重圆，两组相向（Quardruple Circle, four couples in two lines facing）~四重圆，两组相向，一朝方向线，一朝反方向线，同组四人通常为二男二女，男女间隔，舞伴男左女右。<br>
        </p>
        <p>
        三、方舞队形，古代的方舞称为Quadrille，现代美国方舞则称为Square Dance，因此方舞队形在英文中称为Quadrille Formation Square Set或Square Formation，依其时代不同而其队形略有不同，兹述之如下：<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;方舞均系四对一组，成正方形，各对站在正方形之一边，面朝中心，舞伴系男左女右站立，背对音乐台或舞台者为第一对，然后按反时针方向顺序，在古代方舞中依序称为三、二、四对，在现代方舞中则为二、三、四对。<br>
        </p>
        <p>
        四、行列队形，行列舞在法国称为Contre Dance，在英国称为Country Dance，在美国称为Contra或Country，其间的演变有其历史的因素（详阅弦歌土风舞第一集「行列舞来源简介」），另外还有Longways的称呼；因此行列队形也有以下各种不同称法：Longways Formation, Longways Set, Contra Formation, Contra Line-up,Country Formation, Country Line-up, Country Dance Formation等，除非特别说明（如美国黑草莓舞），一般行列舞都是排成二长列，相对而跳，其队伍组织有下列几种：<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（一）男在一列，女在另列，面对正面（前端）时，舞伴男左女右站立。<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（二）两列均为男女间隔，舞伴分别在两列。<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（三）两列均为男女间隔，舞伴在同一列，面对中间时，舞伴男左女右站立。<br>
        </p>
        <p>
        五、自由队形:  英文称为Free Formation，指舞者自由散处于舞场任何地点。通常使用于三人舞或社交舞型的双人舞。<br>
        </p>
