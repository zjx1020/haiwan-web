    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;二、最庄重之男子动作：左腰配剑，左手按于剑鞘下，右手扶剑把而压之，使微微上仰，表示对主宾无敌意；这就是敬礼动作的来源。因此男子的动作，一般是左足退后一步，左手在后，如握剑鞘状，右手在左腰前扶腰部，上体微微前倾，敬礼后，左足向前并于右足旁。<br>
