    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;三、一般女子动作：<br>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（一）四拍动作~双手持裙，右足右踏于第二位直（1）。左足划圈后，置于第四位置，足趾着地，左膝向外弯曲，重心在右足上，右膝弯曲，上体微向前倾，看受礼者（2）。右膝伸直，左足踏到原位（3）。右足并于后第三位置或第一位置（4）。<br>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（二）二拍动作（甲）~双手持裙，右足侧踏于第二位置（l）。左足划圈后在后第四位置踏地，右膝弯曲，上体微向前倾，看受礼者（2）。右膝伸直，左足并于后第三位置或第一位置（3）。<br>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（三）三拍动作（乙）~双手持裙，右足侧踏于第二位置（1）。左足后退，置重心，右足向前伸直，上体微向前倾，看受礼者（2）。置重心于右足上，左足并于右足旁（3）。<br>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;四、一般男子动作：<br>
   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;四拍动作同女子，三拍动作同女子（甲），惟左手置于背后，右手扶于左腰，但更常见的方法是在敬礼时为双是并拢，例如右足侧踏，左足并上，行礼。英国之Honour即指此。<br>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;五、简化之敬礼动作：<br>
   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;二拍之动作是最简单的，也适宜于任何拍数时做简化动作用，任何一足前进一步，另足在后第四位置踏步，前膝伸直，注视受礼者而行礼。做此动作时，一般是双手自然下垂，但若要女子持裙，男子做持剑动作，亦未尝不可，不过若只有二拍，那么大概不够时间去做。有时更简化为双足在第一位置，双手自然下垂而鞠躬，但这样未免太没有舞蹈气氛了，因此稍加改变，男仍照作，而女子将左趾置于右足后，双手持裙，弯双膝而上体前倾。英国之Bob及Cuttsey即指此。<br>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;六、用于美国方舞中之敬礼动作：<br>
   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;美国方舞之特色是活泼、随便、其舞步动作及敬礼亦然，有一种动作见于其中，述之如下：一足前伸，足趾着地（不可足踵着地，那是没礼貌的），重心向后，前足伸直，后足微屈，女双手持裙，男左手置于背后，右手扶左腰，上体前倾而行礼。<br>
   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;若为Balance则指右足起退后二步，再前进二步；使用此一动作时、一般都接上旋转（Swing）的动作。<br>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;七、菲律宾舞的敬礼动作：<br>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在菲律宾舞中，敬礼方式有好几种，通常见到的如下述：<br>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（一）双足并拢于第一位置，双手侧伸于第二位置，上体前倾而行礼。<br>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（二）同（一），惟双手插腰。<br>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（三）左足退后，屈膝，右足向前伸直，足趾着地，上体前倾而行礼；女双手持裙或在第二位置，男双手在第二位置，或插腰，或左手置于背后，右手扶左腰。行礼后，左足向前并于右足旁。<br>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;八、菲律宾舞之邀请动作：<br>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在菲律宾常用手帕、帽子、酒杯来邀请某人，称之为Hapay。<br>
