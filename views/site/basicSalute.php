&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在土风舞中，邀伴、前奏、舞毕时，均有敬礼的机会，也应该敬礼表示礼貌；虽然以一般点头示意行之，也未尝不可，但若以优雅的姿势或有典故的方式来做，则会更有意义；敬礼是一种诚恳的礼貌表示，因此应该有较深度的鞠躬及缓慢的动作，而不宜匆促草率为之。<br>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;敬礼动作，在我国泛称为「敬礼」，但在世界各国则有不同的术语，通常女子的敬礼动作称为Courtsy，另外还有Curtsy, Curtsey, Courtesy等称法，男子之敬礼动作则概称为Bow，男女通用者为Acknowledgment, Address, Solute等；在美国方舞中则称为Honor或Honour，但是有人认为这是较不正式的用法，另外则常用Balance。但在英国的说法，则以为Honour较Bod与curtsey更正式，动作更大。<br>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以下介绍一些敬礼动作：<br>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;一、最庄重的女子动作：这是由欧洲古代宫廷礼仪传下来的，除了敬礼以外，其进场，退场亦有一定的规矩。<br>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（一）进场<br>
   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;若自主宾来看，行礼者自左前方进场，则其左足左踏，右足前交叉，加此反复地进入场中，过程中必须正面对着主宾，其所以横向移动，是为了要注视主宾。若自主宾来看，行礼者自右前方进场，则其右足右踏，左足前交叉，如此反复地进入场中，同样必须正面对着主宾，并注视他。<br>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（二）退场<br>
   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;行礼后的退场，若向右走，则右足右踏，左足后交叉，如此反复地退出，不可侧身对主宾或背朝主宾。若向左走，则左足侧踏，右足后交叉：如此反复地退出。<br>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;若自主宾之正面进场，则向前直走，敬礼后，面对主宝而直退后出场。<br>
   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;至于其敬礼动作，有很严格的规定，其解析如下：<br>
   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（一）向右的敬礼动作：<br>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1、右足向右踏，置重心。<br>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2、左足后交叉，然后双膝弯曲，全身开始下沉。<br>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3、全身下沈，左膝在右膝上自然突出，令身体重量在右足及左趾上。<br>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;4、重心在右足上，上体前倾。<br>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;5、重心于右足上，开始起立。<br>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;6、重心移至左足上，右足侧踏。<br>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以上动作，左手持裙，右手持扇于腰高位置，亦可持裙。<br>
